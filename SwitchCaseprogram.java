package ab55;

public class SwitchCaseprogram {

	public static void main(String[] args) {
		
		switch(1)
		{
		case 1:
			System.out.println("chrome Browser");
			break;
			
		case 2:
			System.out.println("Firefox Browser");
			break;
			
		case 3:
			System.out.println("Safari Browser");
			break;
			
		case 4:
			System.out.println("Edge Browser");
			break;
			
			default : System.out.println("sorry but your selection is wrong");
			System.out.println("Press 1 for chromeBrowser");
			System.out.println("Press 2 for FirefoxBrowser");
			System.out.println("Press 3 for SafariBrowser");
			System.out.println("Press 4 for EdgeBrowser");
		}
			
			
			}

	}


